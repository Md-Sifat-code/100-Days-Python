# Integer type check
a = 123
print(type(a))  # Output: <class 'int'>

# Float type check
b = 12.34
print(type(b))  # Output: <class 'float'>

# Boolean type check
c = True
print(type(c))  # Output: <class 'bool'>

# List type check
d = [1, 2, 3]
print(type(d))  # Output: <class 'list'>


# Convert float to int
b = int(12.34)
print(b)  # Output: 12

# Convert int to float
a = float(123)
print(a)  # Output: 123.0

# Convert list to string
d = str([1, 2, 3])
print(d)  # Output: "[1, 2, 3]"

# Convert string to int
e = int("456")
print(e)  # Output: 456

# Convert int to boolean
f = bool(1)  # Any non-zero integer becomes True
print(f)  # Output: True

# Convert string to list
g = list("Hello")
print(g)  # Output: ['H', 'e', 'l', 'l', 'o']

# Addition
a = 10 + 5
print(a)  # Output: 15

# Subtraction
b = 10 - 5
print(b)  # Output: 5

# Multiplication
c = 10 * 5
print(c)  # Output: 50

# Division (returns a float)
d = 10 / 3
print(d)  # Output: 3.3333333333333335

# Floor Division (integer division that rounds down)
e = 10 // 3
print(e)  # Output: 3

# Modulus (remainder of division)
f = 10 % 3
print(f)  # Output: 1

# Exponentiation (raising to a power)
g = 2 ** 3
print(g)  # Output: 8

# Using the math module for advanced operations
import math

# Square root
h = math.sqrt(16)
print(h)  # Output: 4.0

# Absolute value
i = abs(-10)
print(i)  # Output: 10

# Power function (equivalent to **)
j = math.pow(2, 4)
print(j)  # Output: 16.0

# Trigonometric functions
k = math.sin(math.pi / 2)  # Sine of 90 degrees
print(k)  # Output: 1.0

# Logarithmic functions
l = math.log(10)  # Natural logarithm (base e)
print(l)  # Output: 2.302585092994046

m = math.log10(100)  # Base-10 logarithm
print(m)  # Output: 2.0

# Constants
pi_value = math.pi
print(pi_value)  # Output: 3.141592653589793

e_value = math.e
print(e_value)  # Output: 2.718281828459045

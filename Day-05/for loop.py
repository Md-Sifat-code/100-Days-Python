fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)
# This is the new way of writing the for loop. It's kind of like for each loops in other programming languages


fruits = ["apple", "banana", "cherry"]
for i in range(len(fruits)):
    print(fruits[i])
# This one is the traditional way of writing the for loop where we use range . Both of them works well and perfectly